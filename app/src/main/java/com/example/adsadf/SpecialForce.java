package com.example.adsadf;

public class SpecialForce {
    String url="",description="",title="";

    public SpecialForce(String url, String description, String title) {
        this.url = url;
        this.description = description;
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public String getDescription() {
        return description;
    }

    public String getTitle() {
        return title;
    }
}
